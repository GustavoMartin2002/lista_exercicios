"""3-Faça um algoritmo que leia dois valores inteiros A e B, imprima na tela o quociente e o resto da 
divisão inteira entre eles."""

a = int(input("digite o numero A: "))

b = int(input("digite o numer B: "))

quociente = a / b

resto = a % b

print(f""" 
o quociente entre eles é {quociente}
o resto entre eles é de {resto}
""")